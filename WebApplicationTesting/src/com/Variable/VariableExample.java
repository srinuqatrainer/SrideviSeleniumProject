package com.Variable;

public class VariableExample {

	public static void main(String[] args)
	{
		// Variable is used to save some value
		// Variable's should always be declered with the dataType
		// a Variable can save only one value at a time
		
		int number=10;
		number=30;
		System.out.println(number);
		
		number=40;
		System.out.println(number);
		
		String a="Sridevi";
		System.out.println(a);

		String b="20";
		System.out.println(b);
	}

}
